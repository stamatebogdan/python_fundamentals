print("Range de la 0 la 9")
for i in range(10):
    print(i)

print("Range de la 1 la 9")
for i in range(1,10):
    print(i)

print("Range de la -5 la 4")
for i in range(-5,5):
    print(i)

print("Range de la 10 la 1")
for i in range(10,1,-1):
    print(i)

print("Range de la 0 la 100, din 5 in 5")
for i in range(0,101,5):
    print(i)